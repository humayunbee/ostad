void main() {
  const int a = 7;
  const int b = 3;

  print("a = $a, b = $b");
  
  // Addition
  int add = a + b;
  print("Addition: $a + $b = $add");
  
  // Subtraction
  int sub = a - b;
  print("Subtraction: $a - $b = $sub");
  
  // Multiplication
  int mult = a * b;
  print("Multiplication: $a * $b = $mult");
  
  // Division
  double div = a / b;
  print("Division: $a / $b = $div");
  
  // Modulus
  int mod = a % b;
  print("Modulus: $a % $b = $mod");
}
